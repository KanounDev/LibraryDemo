package com.example.library.model;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;

// This annotation marks this class as a MongoDB document (like a table in SQL).
@Document(collection = "users")
public class User {
    @Id // This is the unique ID field, auto-generated by MongoDB.
    public String id;

    // Username for login.
    public String username;

    // Hashed password (never store plain text!).
    public String password;

    // User's role: MEMBER, LIBRARIAN, or ADMIN.
    public Role role;

    // Library branch (e.g., "Downtown"), required for LIBRARIAN/ADMIN.
    public String branch; // Similar to 'municipality' in your code.
}